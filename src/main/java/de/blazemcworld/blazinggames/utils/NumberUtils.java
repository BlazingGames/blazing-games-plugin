package de.blazemcworld.blazinggames.utils;

public class NumberUtils {
    public static String getRomanNumber(int number) {
        if(number == 0) return "0";
        if(number < 0) return "-" + getRomanNumber(-number);


        return "I".repeat(number)
                .replace("IIIII", "V")
                .replace("IIII", "IV")
                .replace("VV", "X")
                .replace("VIV", "IX")
                .replace("XXXXX", "L")
                .replace("XXXX", "XL")
                .replace("LL", "C")
                .replace("LXL", "XC")
                .replace("CCCCC", "D")
                .replace("CCCC", "CD")
                .replace("DD", "M")
                .replace("DCD", "CM");
    }
}
